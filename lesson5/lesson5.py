def name_check(name: str) -> str:
    """
    Функция, которая проверяет имя на различные ошибки
    и возвращает сообщение с ошибкой.
    """
    message = ""
    if not name:
        message += "Ошибка: пустое имя."
    elif len(name) < 3:
        message += "Ошибка: в имени должно быть минимум 3 символа."
    k = 0

    for symbol in name:
        if symbol == " ":
            k += 1
    if k > 1:
        message += "Ошибка: в имени допускается только 1 пробел."
    return message


def age_check(age: int) -> str:
    """
    Функция, которая проверяет возраст на различные ошибки
    и возвращает сообщение с ошибкой.
    """
    message = ""
    if age <= 0:
        message += "Ошибка: недопустимый возраст."
    elif age < 14:
        message += "Ошибка: минимальный возраст - 14 лет."
    return message


def pass_advice(age: int) -> str | None:
    """
    Функция, которая возвращает совет, если пользователь
    достиг определенного возраста.
    """
    message = ""
    if 16 <= age <= 17:
        message = f"Не забудьте получить свой первый паспорт!"
    elif 25 <= age <= 26:
        message = f"Вам необходимо заменить паспорт!"
    elif 45 <= age <= 46:
        message = f"Вам необходимо заменить паспорт во второй раз!"
    return message


