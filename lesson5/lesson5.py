def main():
    """
    Функция main(), которая требует ввода данных, проверяет их,
    выводит ошибки на экран и возвращает сообщение.
    """
    name = input("Введите ваше имя: ")
    age = int(input("Введите ваш возраст: "))
    while name_check(name) or age_check(age):
        print(f"{name_check(name)} {age_check(age)}")
        name = input("Введите ваше имя: ")
        age = int(input("Введите ваш возраст: "))
    else:
        message = f"Здравствуйте, {name.capitalize()}! Вам {age} лет!"
    return f"{message} {pass_advice(age)}"


print(main())
